// ---------------------------------------------------------------------------
// int fputc( int _c, FILE* _fp )
// ---------------------------------------------------------------------------
#include <os_syscall.h>
#include <rts.h>
// ---------------------------------------------------------------------------
int fputc( int _c, FILE* _fp )
{
  char buf;
  if( _fp == NULL ) return -1;
  buf = (char)_c;
  _fp->ungetc_count = 0;
  if( drv_write( _fp->rid, &buf, 1 ) != 1 ) return -1;
  return ( (int)buf & 0x000000ff );
}
