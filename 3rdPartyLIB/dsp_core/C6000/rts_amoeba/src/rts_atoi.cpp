// ---------------------------------------------------------------------------
// int atoi( const char* st )
// ---------------------------------------------------------------------------
#include <rts.h>
// ---------------------------------------------------------------------------
int atoi( const char* st )
{
  int result = 0;
  char cp;
  int sign;

  while( isspace( (*st) ) ) ++st;

  if( sign = ( ( cp = (*st) ) == '-') ) cp = *++st;
  else if( cp == '+' ) cp = *++st;

  while( isdigit( cp ) )
  {
    result *= 10;
    result += cp - '0';
    cp = *++st;
  }

  if( sign ) result *= -1;
  return result;
}
