#include <rts.h>
// ---------------------------------------------------------------------------
int sem_destroy( sem_t* sem )
{
  if( sem == NULL ) { errno = EINVAL; return -1; }
  if( sem->nauna != 0 ) { errno = EINVAL; return -1; }
  sem_free( sem->rid );
  sem->rid = RES_VOID;
  return 0;
}
