#include <rts.h>
// ---------------------------------------------------------------------------
extern "C" long long _divlli( long long a, long long b );
// ---------------------------------------------------------------------------
extern "C" long long _remlli( long long a, long long b )
{
  return a - _divlli( a, b ) * b;
}
