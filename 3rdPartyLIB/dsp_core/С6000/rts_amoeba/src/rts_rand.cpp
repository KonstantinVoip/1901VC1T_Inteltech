// ---------------------------------------------------------------------------
// int rand()
// ---------------------------------------------------------------------------
#include <rts.h>
// ---------------------------------------------------------------------------
unsigned long __rts_rand_x = 1;
// ---------------------------------------------------------------------------
int rand()
{
  int r;
  __rts_rand_x = __rts_rand_x * 1103515245 + 12345;
  r = (int)( ( __rts_rand_x >> 16 ) & (unsigned long)RAND_MAX );
  return r;
}
