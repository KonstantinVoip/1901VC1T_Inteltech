// int tolower( int c ) // cp1251 version
// ---------------------------------------------------------------------------
int tolower( int c )
{
  if( ( ( c >= 65 ) && ( c <= 90 ) ) || ( ( c >= 192 ) && ( c <= 223 ) ) ) return c + 32;
  if( c == 168 ) return 184;
  return c;
}
