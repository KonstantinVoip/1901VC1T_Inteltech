#include <rts.h>
// ---------------------------------------------------------------------------
void bit_close( BIT_FILE* f )
{
  if( f == NULL ) return;
  if( ( f->mode == 1 ) && ( f->bpos != 0 ) ) fwrite( &f->b, 1, 1, f->f );
  fclose( f->f );
  delete f;
}
