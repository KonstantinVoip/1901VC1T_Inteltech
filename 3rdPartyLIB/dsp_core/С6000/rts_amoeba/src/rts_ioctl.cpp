int32 ioctl( int32 rid, int32 request, void* arg ) // todo: ... args
{
  if( ( rid & RES_T_MASK ) == RES_T_SOCKET )
  {
    return socket_ioctl( rid, request, arg );
  }
  else
  {
    return -1;
  }
//BOS???  return 0;
}
