// ---------------------------------------------------------------------------
// int closedir( DIR* dirp )
// ---------------------------------------------------------------------------
#include <rts.h>
// ---------------------------------------------------------------------------
int closedir( DIR* dirp )
{
  if( dirp == NULL ) { errno = EBADF; return -1; }
  drv_rmd( dirp->rid );
  drv_list_destroy( dirp->d_list );
  delete dirp;
  return 0;
}
