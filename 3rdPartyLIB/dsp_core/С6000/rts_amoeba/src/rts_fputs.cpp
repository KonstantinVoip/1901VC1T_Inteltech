// ---------------------------------------------------------------------------
// int fputs( const char* _ptr, FILE* _fp )
// ---------------------------------------------------------------------------
#include <rts.h>
#include <os_syscall.h>
// ---------------------------------------------------------------------------
int fputs( const char* _ptr, FILE* _fp )
{
  int k, l;
  if( _fp == NULL ) return -1;
  l = strlen( _ptr );
  _fp->ungetc_count = 0;
  if( ( k = drv_write( _fp->rid, _ptr, l ) ) != l ) return -1;
  return k;
}
