// ---------------------------------------------------------------------------
// char *strcpy( register char* dest, register const char* src )
// ---------------------------------------------------------------------------
// ---------------------------------------------------------------------------
#include <rts.h>
// ---------------------------------------------------------------------------
char* strcpy( register char* dest, register const char* src )
{
  register char* d = dest - 1;     
  register const char* s = src  - 1;     
  while( *++d = *++s );
  return dest;
}
