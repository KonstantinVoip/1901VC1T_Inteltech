// ---------------------------------------------------------------------------
// extern void exit( int status )
// ---------------------------------------------------------------------------
#include <rts.h>
// ---------------------------------------------------------------------------

extern int at_exit_count;
extern void (*at_exit_fun[32])();

// ---------------------------------------------------------------------------
void exit( int status )
{
  int i = at_exit_count;
  while( i ) (*at_exit_fun[--i])(); 
  __call_dtors();
  abort();
}
