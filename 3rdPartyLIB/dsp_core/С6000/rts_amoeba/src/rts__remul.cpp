#include <rts.h>
// ---------------------------------------------------------------------------
extern "C" unsigned long _divul( unsigned long x1, unsigned long x2 );
// ---------------------------------------------------------------------------
extern "C" unsigned long _remul( unsigned long a, unsigned long b )
{
  return a - _divul( a, b ) * b;
}
