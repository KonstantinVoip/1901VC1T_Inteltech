// ---------------------------------------------------------------------------
// int32 fclose( FILE* d )
// ---------------------------------------------------------------------------
#include <rts.h>
#include <os_syscall.h>
// ---------------------------------------------------------------------------
int32 fclose( FILE* d )
{
  if( d == NULL ) return -1;
  if( drv_rmd( d->rid ) != 0 ) return -1;
  delete d;
  return 0;
}
